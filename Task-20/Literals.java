package Task20;

public class Literals {
    public static void main(String[] args) {

        // Integer literals
        int decLiteral = 42;
        int octLiteral = 052;
        int hexLiteral = 0xA1;

        // Floating-point literals
        double floatLiteral = 3.14;
        double scientificNotation = 2.5e-3;

        // Character literal
        char charLiteral = 'A';

        // String literal
        String strLiteral = "Hello, World!";

        // Boolean literal
        boolean boolLiteral = true;

        // Null literal
        Object obj = null;

        // Printing the values
        System.out.println("Decimal Literal: " + decLiteral);
        System.out.println("Octal Literal: " + octLiteral);
        System.out.println("Hexadecimal Literal: " + hexLiteral);
        System.out.println("Floating-point Literal: " + floatLiteral);
        System.out.println("Scientific Notation Literal: " + scientificNotation);
        System.out.println("Character Literal: " + charLiteral);
        System.out.println("String Literal: " + strLiteral);
        System.out.println("Boolean Literal: " + boolLiteral);
        System.out.println("Null Literal: " + obj);
    }
}
